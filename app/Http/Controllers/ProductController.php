     */
    public function index()
    {
        //
        $products = new Product;
        $products = $products
            ->select('products.*', 'categories.name as category')
            ->leftJoin('categories', 'categories.id', 'products.category_id')
            ->orderBy('barcode', 'asc')
            ->paginate(15)
        ;


        return response()->json([
            'status' => 'success',
            'code' => 200,
            'products' => $products,
        ]);
